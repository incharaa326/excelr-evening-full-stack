class P 
{
	public static void main(String[] args) 
	{
		//string is a datatype and it is a class we can able represent a group of character within a character in double quotes
		int a, b, c, d;
		float e, f;
		boolean g, h;
		char i, j;
		String k, l;
        //compound assignment 
		a = b = c = d = 10;
		e = f = 10.0f;
		g = h = false;
		i = j = 'a';
		k = l = "xyz";
		System.out.println(a + " " + b + " " + c + " " + d);
		System.out.println(e + " " + f);
		System.out.println(g + " " + h);
		System.out.println(i + " " + j);
		System.out.println(k + " " + l);
	}
}
