class B 
{
	public static void main(String[] args) 
	{
		//post decrement
		int i = 0;
		// the meaning of i--:i=i-1;
		System.out.println(i--);
		System.out.println(i);
		System.out.println(i);
	}
}
/* 
unary operators
++[increment] [post-increment] [pre-inrement]
--[decrement] [post-decrement] [pre-decrement]
*/